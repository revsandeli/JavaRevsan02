package day03variablesContinue;

public class c4_relationalOperators {

    public static void main(String [ ] args){

        //first we create variables whihc is 2 numbers

        byte b1=10;
        byte b2=25;

        boolean example= b1==b2; //false

        //== equal operator
        System.out.println(example);

        //!= (oppesite of equal ) not equal
        System.out.println(b1 != b2);

        // > operator
        System.out.println(b1 > b2);

        // < operator
        System.out.println(b1 < b2);

        // >= operator bigger or equal
        System.out.println(10 >= 11);

        // <= small or equal
        System.out.println(10 <= 15 );

    }
}
